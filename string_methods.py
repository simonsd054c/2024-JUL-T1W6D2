name = "Pat Cummins"

print(len(name))

print(name.upper())
print(name.lower())
print(name.replace("m", "t"))
print(name.replace("Pat", "Tom"))

print(name.split())

splitted_name = name.split()
joined_name = ".".join(splitted_name)

print(joined_name)